/**
 * Semantic typography getters
 * Use these for better semantics and maintainability
 */
export const getTypography = (variant: keyof typeof textStyles) => {
  return textStyles[variant];
};

/**
 * Helper function to combine typography with custom classes
 */
export const mergeTypographyClasses = (
  typographyVariant: keyof typeof textStyles,
  additionalClasses?: string
): string => {
  const baseClasses = textStyles[typographyVariant].className;
  return additionalClasses
    ? `${baseClasses} ${additionalClasses}`
    : baseClasses;
};
